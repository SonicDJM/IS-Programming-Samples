public class sumArray
{ 
  public static int sumArray(int[] a)
  {
    int sum = 0;
    for (int i=0; i < a.length; i++)
    {
      sum = sum + a[i];
    }
    return sum;
  }
  
  public static void main(String[] args)
  { 
    int[] a = new int[]{1,5,10,25,50,100};
    int value = sumArray(a);
    System.out.println(value);
  }
}
